package com.Loop;
// to find odd numbers between 30 to 20
public class ForLoop3 {

	public static void main(String[] args) 
	{
		System.out.println("Odd numbers are:");
        for(int i=30; i>=20; i--)
        {
    	   if(i%2==1)
    	   {
    		   System.out.println(i+ " ");
    	   }
       }

	}

}
